#!/usr/bin/env node
/**
 * Bootstrap Expected
 * ------------------
 * ใช้ Gemini /api/receipts/analyze เป็น draft ground truth
 * แล้ว save เป็น tests/expected/<bill>.json — คุณค่อยเปิดดูรูปแล้วแก้ที่ผิด
 *
 * ใช้ครั้งเดียวตอนเริ่ม — เพื่อไม่ต้องพิมพ์ expected เองทุกใบ
 *
 * Usage:
 *   ACCESS_CODE=xxx node tests/bootstrap-expected.mjs
 *   ACCESS_CODE=xxx node tests/bootstrap-expected.mjs --overwrite
 */

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const APP_URL = process.env.APP_URL ?? 'http://localhost:3000';
const ACCESS_CODE = process.env.ACCESS_CODE;
const BILL_DIR = path.resolve(ROOT, process.env.BILL_DIR ?? 'Bill');
const EXPECTED_DIR = path.join(__dirname, 'expected');

const args = Object.fromEntries(process.argv.slice(2).map((a) => {
  const [k, v] = a.replace(/^--/, '').split('=');
  return [k, v ?? true];
}));
const OVERWRITE = !!args.overwrite;

if (!ACCESS_CODE) { console.error('❌ set ACCESS_CODE'); process.exit(1); }

const IMG_EXT = new Set(['.jpg', '.jpeg', '.png', '.webp', '.bmp', '.heic']);

async function login() {
  const res = await fetch(`${APP_URL}/api/login`, {
    method: 'POST', headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ pass: ACCESS_CODE }),
  });
  if (!res.ok) throw new Error(`login failed: ${res.status}`);
  return res.headers.get('set-cookie').split(';')[0];
}

const MIME = {
  '.jpg': 'image/jpeg', '.jpeg': 'image/jpeg', '.png': 'image/png',
  '.webp': 'image/webp', '.bmp': 'image/bmp', '.heic': 'image/heic',
};

async function analyze(filePath, cookie) {
  const buf = await fs.readFile(filePath);
  const ext = path.extname(filePath).toLowerCase();
  const fd = new FormData();
  fd.append('receipt', new Blob([buf], { type: MIME[ext] ?? 'application/octet-stream' }), path.basename(filePath));
  const res = await fetch(`${APP_URL}/api/receipts/analyze`, {
    method: 'POST', headers: { Cookie: cookie }, body: fd,
  });
  if (!res.ok) throw new Error(`analyze ${res.status}: ${await res.text()}`);
  return res.json();
}

function toExpected(geminiResult) {
  const expense = geminiResult.receipt ?? geminiResult.expense ?? geminiResult.data ?? geminiResult;
  return {
    _draft: 'auto-generated by Gemini — โปรดเปิดดูรูปแล้วแก้ field ที่ผิด แล้วลบ _draft ทิ้ง',
    products: (expense.products ?? expense.items ?? []).map((p) => ({
      product: p.product ?? p.name,
      qty: Number(p.qty ?? p.quantity ?? 1),
      price: Number(p.price ?? p.amount ?? p.total ?? 0),
    })),
    total: Number(expense.total ?? 0) || null,
  };
}

async function run() {
  await fs.mkdir(EXPECTED_DIR, { recursive: true });
  const files = (await fs.readdir(BILL_DIR))
    .filter((f) => IMG_EXT.has(path.extname(f).toLowerCase())).sort();

  console.log(`🔐 login...`);
  const cookie = await login();

  for (const f of files) {
    const stem = path.basename(f, path.extname(f));
    const outPath = path.join(EXPECTED_DIR, `${stem}.json`);
    if (!OVERWRITE) {
      try { await fs.access(outPath); console.log(`⏭  ${stem}  (มีอยู่แล้ว)`); continue; } catch {}
    }
    process.stdout.write(`▶ ${f} ... `);
    try {
      const res = await analyze(path.join(BILL_DIR, f), cookie);
      const expected = toExpected(res);
      await fs.writeFile(outPath, JSON.stringify(expected, null, 2));
      console.log(`✓ (${expected.products.length} items, total=${expected.total})`);
    } catch (e) {
      console.log(`ERR ${e.message}`);
    }
  }
  console.log(`\n📝 ทุกไฟล์อยู่ที่ ${path.relative(ROOT, EXPECTED_DIR)}/`);
  console.log(`   เปิดแต่ละไฟล์ → ดูรูปบิลเทียบ → แก้ field ที่ผิด → ลบ _draft → save`);
}

run().catch((e) => { console.error(e); process.exit(1); });
